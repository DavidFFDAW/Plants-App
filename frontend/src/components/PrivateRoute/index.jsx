export function PrivateRoute({ children, ...rest }) {
    // Hook for user loggedIn check
     const auth = true;

    return (
      <Route
        {...rest}
        render={({ location }) =>
          auth.user ? (
            children
          ) : (
            <Redirect
              to={{
                pathname: "/login",
                state: { from: location }
              }}
            />
          )
        }
      />
    );
}